package TejasSir_javarev.basics;
public class BasicCalc {
	public static void main(String[] args) {
		int a =3,b=4;
		System.out.println(a+" + "+b+"="+(a+b));
		System.out.println(a+" + "+b+"="+(a-b));
		System.out.println(a+" + "+b+"="+(a*b));
		System.out.println(a+" + "+b+"="+(a/b));
		System.out.println(a+" + "+b+"="+(a%b));
	}
}
